void main() {
  //การสร้างลีสใหม่ขึ้นจากลีสเก่าโดยเพิ่มเติมสิ่งที่ต้องการบางอย่างเข้ามา
  //ค่าที่ได้จากการ map จะมีค่าออกมาเป็น Interable ซึ่งสามารถนำไปทำการวนซ้ำได้
  //โดยยังไม่มีชนิดของข้อมูล(generic)ดังนั้นจึงมีการเอา method อื่นมาต่อท้ายเพิ่อให้มีคุณสมบัติตามต้องการ
  //ปกติจะต่อท้ายด้วย toList() แต่ก็สามารถต่อท้ายได้หลายอย่างตามความต้องการ

  List<String> cities = ['Bankok', 'Moscow', 'USA', 'London','USA'];
  var x=cities.map((e) =>'Country of' +e).toSet().toList();
  print(x);
}
